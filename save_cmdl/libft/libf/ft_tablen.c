#include "../inc/libftprintf.h"

int		ft_tablen(char **tab)
{
	int		i;

	i = 1;
	if (tab && tab[0])
	{
		while (tab[i] != NULL)
			i++;
		return (i);
	}
	else
		return (0);
}
