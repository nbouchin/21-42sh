#include "header.h"

int		esc(t_cmdl *cmdl)
{
	if (cmdl->opt & CCOMP)
	{
		cmdl->opt &= ~(CCOMP);
		tputs(tgetstr("cd", NULL), 1, ft_putchar);
	}
	return (1);
}
