#include "../inc/libftprintf.h"

int			ft_strdigit(char *str)
{
	int		i;

	i = -1;
	if (!str)
		return (0);
	while (str[++i])
		if (str[i] < '0' || str[i] > '9')
			return (0);
	return (1);
}
