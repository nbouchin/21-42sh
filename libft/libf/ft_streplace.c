#include "../inc/libftprintf.h"

void		ft_streplace(char **str, char c, char r)
{
	int		i;

	i = -1;
	if (!str || !c)
		return ;
	while ((*str)[++i])
		if ((*str)[i] == c)
			(*str)[i] = r;
}
