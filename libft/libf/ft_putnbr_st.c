#include "../inc/libftprintf.h"

void	ft_putnbr_st(size_t n)
{
	unsigned int	nb;

	nb = n;
	if (nb > 9)
		ft_putnbr(nb / 10);
	ft_putchar('0' + nb % 10);
}
