#include "../inc/libftprintf.h"

void	ft_buffill(t_b *b, char *tmp)
{
	int		i;

	i = -1;
	while (tmp[++i] != '\0')
	{
		if (b->pos == 1023)
			ft_reset(b);
		b->buff[b->pos++] = tmp[i];
	}
}
